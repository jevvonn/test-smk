/**
 * Menghasilkan array baru yang merupakan seluruh elemen
 * bilangan genap dari array numbers
 * @namespace Latihan
 * @method filterEvenNumbers
 * @return {array} result array berisi bilangan genap dari array lama numbers
 */
function filterEvenNumbers(numbers) {
  // TODO: Buat array baru yang berisi seluruh nilai belangan genap dari array lama numbers
  return [...numbers].filter((num) => num % 2 == 0);
}

module.exports = filterEvenNumbers;
